#include <iostream>
using namespace std;

int main() {
    int a = 'a';
    cout << a << endl; // This will print the ASCII value of 'a', which is 97

    char b = 106;
    cout << b << endl; // This will print the character corresponding to ASCII value 106, which is 'j'  

    int c = -10;
    cout << c << endl; // This will print -10

    unsigned int d = -32;
    cout << d << endl; // This will print a large positive number due to unsigned representation
    
}